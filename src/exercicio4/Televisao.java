package exercicio4;

public class Televisao {
    private int channel;
    private int volume;

    public Televisao() {
        this.channel = 1;
        this.volume = 20;
    }

    public int getChannel() {
        return channel;
    }

    public void setChannel(int channel) {
        this.channel = channel;
    }

    public int getVolume() {
        return volume;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }
}
